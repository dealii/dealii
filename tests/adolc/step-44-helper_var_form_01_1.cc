// This is a modified version of step-44, which tests the implementation of
// cell-level auto-differentiation (via a variational formulation).
//
// AD number type: ADOL-C taped

#include "../tests.h"

#include "../ad_common_tests/step-44-helper_var_form_01.h"

int
main(int argc, char **argv)
{
  initlog();

  Utilities::MPI::MPI_InitFinalize mpi_initialization(
    argc, argv, testing_max_num_threads());

  namespace AD = dealii::Differentiation::AD;
  using namespace Step44;
  try
    {
      const unsigned int                               dim = 2;
      Solid<dim, double, AD::NumberTypes::adolc_taped> solid(
        SOURCE_DIR "/prm/parameters-step-44.prm");
      solid.run();
    }
  catch (std::exception &exc)
    {
      std::cerr << std::endl
                << std::endl
                << "----------------------------------------------------"
                << std::endl;
      std::cerr << "Exception on processing: " << std::endl
                << exc.what() << std::endl
                << "Aborting!" << std::endl
                << "----------------------------------------------------"
                << std::endl;
      return 1;
    }
  catch (...)
    {
      std::cerr << std::endl
                << std::endl
                << "----------------------------------------------------"
                << std::endl;
      std::cerr << "Unknown exception!" << std::endl
                << "Aborting!" << std::endl
                << "----------------------------------------------------"
                << std::endl;
      return 1;
    }
  return 0;
}
