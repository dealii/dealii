// Test the support points of FE_WedgeP for consistency with the support points
// generated by a triangle and a line.

#include <deal.II/fe/fe_simplex_p.h>
#include <deal.II/fe/fe_wedge_p.h>

#include "../tests.h"


template <int dim>
std::vector<Point<dim>>
unordered_support_points(const unsigned int degree)
{
  std::vector<Point<dim>> unit_points;

  FE_SimplexP<2> fe_triangle(degree);
  FE_SimplexP<1> fe_line(degree);

  for (unsigned int i = 0; i < fe_triangle.n_dofs_per_cell(); ++i)
    for (unsigned int j = 0; j < fe_line.n_dofs_per_cell(); ++j)
      unit_points.emplace_back(fe_triangle.unit_support_point(i)[0],
                               fe_triangle.unit_support_point(i)[1],
                               fe_line.unit_support_point(j)[0]);
  return unit_points;
}

template <int dim>
void
test(const unsigned int degree)
{
  deallog << "Support points of degree " << degree << std::endl;
  const auto fe             = FE_WedgeP<dim>(degree);
  const auto support_points = fe.get_unit_support_points();

  const unsigned int n = (degree + 1) * (degree + 1) * (degree + 2) / 2;

  if (support_points.size() - n == 0)
    deallog << "Correct size" << std::endl;

  const auto unordered_points = unordered_support_points<dim>(degree);

  bool consistent = true;
  for (const auto &p : support_points)
    {
      bool found_node = false;
      for (const auto &unordered_point : unordered_points)
        if (p.distance(unordered_point) < 1e-12)
          found_node = true;

      if (found_node == false)
        consistent = false;
    }

  if (consistent)
    deallog << "All nodes correct" << std::endl;
  else
    deallog << "Error in the nodes" << std::endl;

  deallog << std::endl;
}


int
main()
{
  initlog();

  deallog.push("3D");
  for (unsigned int i = 1; i < 3; ++i)
    test<3>(i);
  deallog.pop();
}
